// continue
//Use to skip Some Values

for(let i = 0 ; i<=10 ; i++){
    if(i==3){
        continue; //Move toward the Next Iteration it means 3 will skipped
    }
    console.log("The Value of i = ",i);
}


// Break

for(let i = 0 ; i<=10 ; i++){
    if(i==7){
        break; //This will stop the loop here , this will breakpoint of loop
    }
    console.log("The Value of i = ",i);
}